/* USER CODE BEGIN 4 */
void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin){

	//Left side pulse
	if(GPIO_Pin == GPIO_PIN_4){

		//count encoder pulses
		if(HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_6) == GPIO_PIN_SET) {
			leftPulse -= 1;
		} else {
			leftPulse += 1;
		}
	}

	//Right side pulse
	else if(GPIO_Pin == GPIO_PIN_0){

		//count encoder pulses
		if(HAL_GPIO_ReadPin(GPIOB, GPIO_PIN_1) == GPIO_PIN_SET) {
			rightPulse += 1;
		} else {
			rightPulse -= 1;
		}

	}
}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
  // Check which version of the timer triggered this callback and toggle LED
  if (htim == &htim3 )
  {
    float leftRPM = leftPulse * SECS_PER_MIN/PULSES_PER_ROTATION;
    float rightRPM = rightPulse * SECS_PER_MIN/PULSES_PER_ROTATION;
    float leftAngular = leftRPM * rpmToAngular;
    float rightAngular = rightRPM * rpmToAngular;
    leftVelocity = leftAngular * wheelRad;
    rightVelocity = rightAngular * wheelRad;

    velocities[0] = leftVelocity;
    velocities[1] = rightVelocity;

    // change huartX to your initialized HAL UART peripheral
    HAL_UART_Transmit(&huart2, (uint8_t *)&velocities, sizeof(velocities), HAL_MAX_DELAY);

    leftPulse = 0;
    rightPulse = 0;

  }
}
